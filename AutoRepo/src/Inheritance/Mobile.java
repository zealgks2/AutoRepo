package Inheritance;

public class Mobile extends Telephone {

	
	public void texting()
	{
		
		System.out.println("texting from mobile");
		
		
		
	}
	
	
	public void calling()
	{
		System.out.println("calling from mobile");

	}
	
	
	/*whenever we have two methods with same
	parameter in parent and child class then both methods are overridden methods*/
	
	
	
}
